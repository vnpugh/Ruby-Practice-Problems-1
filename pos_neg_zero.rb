# Write a Ruby script that checks if a number is positive, negative, or zero. 
# If it is a positive number, print "positive". 
# If it is a negative number, print "negative". Otherwise, print "zero".


number = 20 

if number > 0
  puts "positive"
elsif number < 0
  puts "negative"
else
  puts "zero"
end

# Run -> Ruby pos_neg_zero.rb
# Output -> positive
