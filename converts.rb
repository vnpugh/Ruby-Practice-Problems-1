# Write a ruby script that converts the string "123" to an integer.
string_num = "123"

integer_num = string_num.to_i

puts "The string converted to an integer: #{integer_num}"
# Output -> The string converted to an integer: 123




# Write a ruby script that converts the integer 123 to a string.
integer_num = 123

string_num = integer_num.to_s

puts "The integer converted to a string: \"#{string_num}\""
# Output -> The integer converted to a string: "123"





#Write a ruby script that converts the string "123.45" to a float.

string_num = "123.45"

float_num = string_num.to_f

puts "The string converted to a float: #{float_num}"
# Output -> The string converted to a float: 123.45
